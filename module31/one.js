//  we learn here about primitive and non primitive

/*
Primitive: Basic data type whose values are immutable and compared by value (Number, String, Boolean, Null, Undefined, Symbol, BigInt).
Non-Primitive: Complex data type stored by reference, mutable, includes Object, Array, and Function.

Mutable vs Immutable: Mutable values can be changed after creation (objects, arrays); immutable values cannot be changed once created (primitives).
*/
